    /**
     * Taban uzunluğu ve yüksekliği verilen bir üçgenin alanını hesaplayıp döndürür.
     * Alan = (a * h) / 2
     * @param a Üçgenin taban uzunluğu
     * @param h Üçgenin yüksekliği
     * @return Üçgenin alanı
     */
    public double triangleArea(double a, double h) {
        return (a * h) / 2.0;
    }
